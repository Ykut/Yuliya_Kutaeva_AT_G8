package pretask;

public class MathOperatorsExample {
    public static void main(String[] args) {
        int fNum = 6;
        int sNum = 3;

        System.out.println("Сложение: "+ (fNum+sNum));
        System.out.println("Вычитание: "+ (fNum-sNum));
        System.out.println("Умножение: "+ fNum*sNum);
        System.out.println("Деление: " + fNum/sNum);

    }
}
